package com.example.cityguild.entity;

public class DoctorEntity {
private String docid;
private String docimageurl;
private String cpid;
private String doclevel;
private String docname;
public String getDocid() {
	return docid;
}
public void setDocid(String docid) {
	this.docid = docid;
}
public String getDocimageurl() {
	return docimageurl;
}
public void setDocimageurl(String docimageurl) {
	this.docimageurl = docimageurl;
}
public String getCpid() {
	return cpid;
}
public void setCpid(String cpid) {
	this.cpid = cpid;
}
public String getDoclevel() {
	return doclevel;
}
public void setDoclevel(String doclevel) {
	this.doclevel = doclevel;
}
public String getDocname() {
	return docname;
}
public void setDocname(String docname) {
	this.docname = docname;
}
}
